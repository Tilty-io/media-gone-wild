<section class="mb-8 rounded-box border border-base-300 bg-base-100 p-6 shadow-sm">
    <h2 class="text-xl font-semibold">Photos : paramètres à connaître</h2>
    <p class="mt-2 text-sm opacity-80">
        Ces paramètres s'appliquent à <code class="rounded bg-base-200 px-1 py-0.5">/photo</code> :
        <code class="rounded bg-base-200 px-1 py-0.5">width</code>,
        <code class="rounded bg-base-200 px-1 py-0.5">height</code>,
        <code class="rounded bg-base-200 px-1 py-0.5">fit</code>,
        <code class="rounded bg-base-200 px-1 py-0.5">extension</code>,
        <code class="rounded bg-base-200 px-1 py-0.5">quality</code> et
        <code class="rounded bg-base-200 px-1 py-0.5">bgcolor</code>.
    </p>
    <p class="mt-2 text-sm opacity-80">
        Sur <code class="rounded bg-base-200 px-1 py-0.5">/video</code> et
        <code class="rounded bg-base-200 px-1 py-0.5">/logo</code>, ils sont ignorés.
    </p>

    <p class="mt-2 text-sm opacity-80">
        Astuce rapide : combine <code class="rounded bg-base-200 px-1 py-0.5">width</code> + <code class="rounded bg-base-200 px-1 py-0.5">height</code> + <code class="rounded bg-base-200 px-1 py-0.5">fit</code>
        pour maîtriser le cadrage, puis ajoute <code class="rounded bg-base-200 px-1 py-0.5">extension=png</code> et
        <code class="rounded bg-base-200 px-1 py-0.5">bgcolor=ffffff00</code> quand tu as besoin d'un fond transparent.
    </p>
</section>

